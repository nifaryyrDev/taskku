<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use App\Models\User;

class Task extends Model
{
    use HasFactory;

    /*
    |--------------------------------------------------------------------------
    | Nama Table
    |--------------------------------------------------------------------------
    */

    protected $table = 'tasks';

    /*
    |--------------------------------------------------------------------------
    | Fillable
    |--------------------------------------------------------------------------
    */

    protected $fillable = [
        'user_id',
        'title',
        'file',
    ];

    /*
    |--------------------------------------------------------------------------
    | Hidden
    |--------------------------------------------------------------------------
    */

    protected $hidden = [
        //
    ];

    /*
    |--------------------------------------------------------------------------
    | Casting
    |--------------------------------------------------------------------------
    */

    protected $casts = [
        'created_at' => 'datetime',
        'updated_at' => 'datetime',
    ];

    /*
    |--------------------------------------------------------------------------
    | Relasi User
    |--------------------------------------------------------------------------
    */

    public function user()
    {
        return $this->belongsTo(User::class);
    }

    /*
    |--------------------------------------------------------------------------
    | Accessor Nama File
    |--------------------------------------------------------------------------
    */

    public function getFileUrlAttribute()
    {
        if ($this->file) {

            return asset('files/' . $this->file);
        }

        return null;
    }

    /*
    |--------------------------------------------------------------------------
    | Scope User
    |--------------------------------------------------------------------------
    */

    public function scopeUserTasks($query)
    {
        return $query->where('user_id', auth()->id());
    }

    /*
    |--------------------------------------------------------------------------
    | Scope Latest
    |--------------------------------------------------------------------------
    */

    public function scopeLatestTasks($query)
    {
        return $query->latest();
    }
}